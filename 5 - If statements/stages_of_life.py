person_age = 7

if 0 <= person_age < 2:
    print("You are a baby")
elif 4 > person_age >= 2:
    print("You are a toddler")
elif 13 > person_age >= 4:
    print("You are a kid")
elif 20 > person_age >= 13:
    print("You are a teenager")
elif 65 > person_age >= 20:
    print("You are a adult")
elif 120 > person_age >= 65:
    print("You are a elder")
else:
    print("Invalid age")
