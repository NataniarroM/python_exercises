locations = ["Rome", "Bueno Aires", "Tokyo", "Paris", "Rio de Janeiro"]

print(sorted(locations))
print(locations)
print("\n")

print(sorted(locations, reverse=True))
print(locations)
print("\n")

locations.reverse()
print(locations)
print("\n")

locations.reverse()
print(locations)
print("\n")

locations.sort()
print(locations)
print("\n")

locations.sort(reverse=True)
print(locations)
print("\n")
